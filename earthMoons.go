package main
import "fmt"

func main(){
	var planetName string  // var (name of variable) (type of variable)
	var numberOfMoons int // variable: var name: number of moons type: int

	planetName = "Earth" // Assigning the variable planetName as Earth
	numberOfMoons = 1 // Assigning the variable numberOfMoons the number one
	fmt.Println(planetName, numberOfMoons) // Shows both the planetName variable and the numberOfMoons
}

// OUTPUT: Earth 1