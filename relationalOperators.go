package main
import "fmt"

func main(){

	// RELATIONAL OPERATORS

	var i int = 4
	var j int = 2
	
	fmt.Println("i < j = ", i<j)
	// OUTPUT: i < j = false
	fmt.Println("i > j = ", i>j)
	// OUTPUT: i > j = true
	fmt.Println("i >= j = ", i>=j)
	// OUTPUT: i >= j = true
	fmt.Println("i <= j = ", i<=j)
	// OUTPUT: i <= j = false	
}