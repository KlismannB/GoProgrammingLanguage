package main
import "fmt"

func main(){

	// MULTIPLICATION AND DIVISOR OPERATOR
	var i int = 20
	var j int = 10

	fmt.Println("i * j = ", i*j)
	// OUTPUT: i * j = 200
	fmt.Println("i / j", i/j)
	// OUTPUT: i / j = 2

	fmt.Println("i % j = ", i%j)
	/*  For those who never programmed anything,
		this operator % means the remainder of 
		the integer division between those two numbers.
	*/
	// OUTPUT: i % j = 0

}